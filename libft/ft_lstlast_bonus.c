#include "libft.h"

t_list		*ft_lstlast(t_list *lst)
{
	t_list		*gon;

	gon = lst;
	while (gon != 0)
	{
		if (gon->next == 0)
			return (gon);
		gon = gon->next;
	}
	return (0);
}
